export const shuffleWord = (word: string[], maxWord: number) => {
  const shuffledWord = word.slice(0)
  for (let i = shuffledWord.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1))
    const temp = shuffledWord[i]
    shuffledWord[i] = shuffledWord[j]
    shuffledWord[j] = temp
  }
  return shuffledWord.slice(0, maxWord)
}
